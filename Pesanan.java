public class Pesanan {// Class untuk menyimpan data pesanan
    // kode unik pesanan
    int kodePesanan;
    // nama menu yang dipesan
    String namaPesanan;
    // harga pesanan
    int harga;

    // constructor untuk mengisi data pesanan
    Pesanan(int kodePesanan, String namaPesanan, int harga) {
        this.kodePesanan = kodePesanan;
        this.namaPesanan = namaPesanan;
        this.harga = harga;
    }
}
    

